package com.ava.cookbook.models;

import java.io.Serializable;
import javax.persistence.*;

@Entity
public class User implements Serializable {

    @Id @GeneratedValue
    long id;            // Unique identifier of the user in the DB
    String username;    // Unique name used for login
    String password;    // Password used for login
    String avatarPath;  // Absolute path to the image file used as avatar

    public User() {

    }

    public User(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAvatarPath() {
        return avatarPath;
    }

    public void setAvatarPath(String avatarPath) {
        this.avatarPath = avatarPath;
    }
}
